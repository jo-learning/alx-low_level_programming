
/**
 * add - add two numbers
 * @a: number
 * @b: number
 *
 * Return: sum of the numbers
 */

int add(int a, int b)
{
	return a + b;
}

/**
 * sub - subtraction two numbers
 * @a: number
 * @b: number
 *
 * Return: sub of the numbers
 */

int sub(int a, int b)
{
        return a - b;
}

/**
 * mul - multiple two numbers
 * @a: number
 * @b: number
 *
 * Return: multiple of the numbers
 */

int mul(int a, int b)
{
        return a * b;
}

/**
 * div - division two numbers
 * @a: number
 * @b: number
 *
 * Return: sum of the numbers
 */

int div(int a, int b)
{
        return a / b;
}
/**
 * mod - modulo two numbers
 * @a: number
 * @b: number
 *
 * Return: modulo of the numbers
 */

int mod(int a, int b)
{
	return a % b;
}
